//Assigning means equal to

let x=10;
console.log("intial value of x:",x);

//using +=
x+=5  ; //x=x+5
console.log("after x += 5  :", x);

//using -=
x-=5 ;  //x=x-5
console.log("after x -= 5 :", x);


//using *=
x*=5   ;//x=x*5
console.log("after x *= 5 :", x);
 
//using /=
x/=5; // x=x/5
console.log("after x/=5 :", x);

//using %=
x%=5;//
console.log("after x%=5:", x);